public class VowelConsonant {
	public static void main(String[] args) {
		char check = 'b';
		
		if(check == 'a' || check == 'e' || check == 'i' || check == 'o' || check == 'u')

		System.out.println(check + " character is vowel");
		else
			System.out.println(check + " character is consonant");
	}
	
}
